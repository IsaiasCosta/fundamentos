//Tipos de Atribuição
const a = 8
let b = 9

b += a // atribuição aditiva - atribuição com adição - b=b+a
console.log(b)

b -= 5 // atribuição substrativa - b=b-5
console.log(b)
b*=7 //atribuição muitipliactiva - b=b*7
console.log(b)
b/=8 // atribuição divisiva  - b=b/8
console.log(b)

b%=4 //atribuição modular - b=b%4
console.log(b)
